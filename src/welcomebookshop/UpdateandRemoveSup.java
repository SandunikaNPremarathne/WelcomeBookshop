    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
  try{
            SupplierDetails st=new SupplierDetails();
            st.setSupId(txt1.getText());
            st.setTele(Integer.parseInt(txt6.getText().toString()));
            st.setSalesRep(txt5.getText());

            boolean result = dbOps.UpdateSupplier(st);

            if(result){
                JOptionPane.showMessageDialog(this, "Successfull");
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Enter inputs");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Enter valid inputs");

        }
                     
    }//GEN-LAST:event_jButton1ActionPerformed

