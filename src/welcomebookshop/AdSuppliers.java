public void setSupplier(String s){
    DefaultTableModel model = (DefaultTableModel) Table1.getModel();
       model.setRowCount(0);
      try{
            con=(Connection)DriverManager.getConnection(url, username, password);
            String query1="SELECT * FROM suppliers where catagory='"+s+"'";
            pst =(PreparedStatement)con.prepareStatement(query1);
            rs= pst.executeQuery();
            while(rs.next()){
                String supId = rs.getString(1);
                String name = rs.getString(2);
                String Address = rs.getString(4);
                String email = rs.getString(5);
                String salesRep = rs.getString(6);
                String tele=rs.getString(7);
                model.addRow(new Object[] {supId,name,Address,email,salesRep,tele});
                }
               
      }
      catch(Exception e){
           System.out.println(e);
      }
       finally{
            try{
            if(pst!=null){
                pst.close();
            }
            if(con!=null){
                con.close();
            }
            }
            catch(Exception e){
                
            }
      }
}
