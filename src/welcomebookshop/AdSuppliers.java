    private void btn9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn9ActionPerformed
int row=Table1.getSelectedRow();
           if(row>-1){
               SupplierDetails st=new SupplierDetails();
               String value=Table1.getModel().getValueAt(row, 0).toString();
                st.setSupId(value);
                int response=JOptionPane.showConfirmDialog(rootPane, "Do you want to Remove?", "Remove", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
                if(response==JOptionPane.YES_OPTION){
                   boolean result = dbOps.RemoveSupplier(st);
        }
           }
           else
               JOptionPane.showMessageDialog(null, "Select a Supplier to remove");
    }//GEN-LAST:event_btn9ActionPerformed

